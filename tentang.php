<p>Sistem pakar diagnosa penyakit pada ayam ini merupakan sebuah sistem yang dapat membantu para peternak  dalam mendiagnosa penyakit ayam.
sistem ini menggunakan basis pengetahuan yang didapatkan dari beberapa pakar yaitu para dokter hewan.</p>
<p>Sistem pakar ini menggunakan metode Case Based Reasoning (CBR) yg berbasis penalaran kasus yang dapat memberikan output berupa penyakit apa saja yang diderita oleh ayam berdasarkan gejala yang diinput.</p>
<p>Rancangan sistem ini merupakan salah satu syarat dalam penyelesaian Gelar Sarjana (Strata 1 - S1).</p>
