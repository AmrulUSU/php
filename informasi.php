                                  <h3>Mengenal Jenis-jenis Ayam</h3>
                                      <div dir="ltr" trbidi="on">Ayam atau nama latinya <em>Gallus gallus domesticus</em> adalah jenis unggas paling populer untuk diternakan. Selain mudah perawatanya ayam juga mudah sekali untuk dikembang biakan. Lalu jenis ayam apa saja yang bisa dibudidayakan khususnya di Indonesia dengan iklim tropis ? <br />
                                        <br />
                                        Di Indonesia dikenal istilah ayam ras dan ayam bukan ras (buras, atau kampung). Dalam pengertian &quot;ayam ras&quot; menurut istilah itu yang dimaksud sebenarnya adalah ras yang dikembangkan untuk usaha komersial massal, seperti Leghorn (&quot;lehor&quot;). Ke dalam kelompok ayam buras terdapat pula ras lokal ayam yang khas namun tidak dikembangkan untuk usaha komersial massal. Ayam-ayam ras lokal demikian sekarang mulai dikembangkan (dimurnikan) sebagai ayam sabung, ayam timangan (pet), atau untuk acara ritual. Berikut ini adalah ras lokal ayam di Nusantara yang telah dikembangkan untuk sifat/penampilan tertentu:<br />
                                        <br />
                                        <br />
                                        <ul>
                                          <li>ayam pelung, ras lokal dan unggul dari Priangan (Kabupaten Cianjur) yang memiliki kokokan yang khas (panjang dan bernada unik), termasuk ayam hias;</li>
                                          <li>ayam kedu (termasuk ayam cemani), ras lokal dan mulia dari daerah Kedu dengan ciri khas warna hitam legam hingga moncong dan dagingnya, termasuk ayam pedaging dan ayam hias;</li>
                                          <li>ayam nunukan, ras lokal dan mulia dari Nunukan, Kaltim, dengan bentuk badan tegap dan ukuran besar, keturunan ayam aduan, termasuk ayam pedaging dan hias;</li>
                                        </ul>
                                        <br />
                                        <a name="more" id="more"></a><br />
                                        1. Ayam Kedu<br />
                                        <br />
                                        Ayam kedu merupakan ayam lokal yang berkembang di Kabupaten Magelang dan Temanggung atau eks. Kersidenan Kedu (Jawa Tengah). Berdasarkan penampilan warnanya, ayam kedu dapat dibedakan menjadi empat jenis sebagai berikut.<br />
                                        <br />
                                        a. Ayam Kedu Hitam<br />
                                        <br />
                                        <div><a href="images/Ayam kedu.jpg" alt="" width="320" height="240" border="0" /></a></div>
                                        <br />
                                        Ayam kedu hitam mempunyai penampilan fisik hamper hitam semua, tetapi kalau diamati secara teliti warnanya tidak terlalu hitam . Penampilan kulit pantat dan jengger masih mengandung warna kemerah-merahan. Bobot ayam kedu hitam jantan dewasa antara 2 Kg – 2,5 Kg, sedangkan yang betinanya hanya 1,5 Kg. Ayam ini sering disamakan dengan ayam cemani karena tampak serba hitam.<br />
                                        <br />
                                        b. Ayam Kedu Cemani<br />
                                        <br />
                                        <div><a href="http://2.bp.blogspot.com/-6jb8-qg9JGg/TtHjuhfIJaI/AAAAAAAABpA/ruPubijz74w/s1600/Ayam%2BKedu%2BCemani.jpg" imageanchor="1"><img src="images/Ayam kedu.jpg" alt="" width="320" height="240" border="0" /></a></div>
                                        <br />
                                        Ayam kedu cemani memiliki penampilan sosok tubuh hitam mulus, termasuk paruh, kuku, telapak kaki, lidah, telak (langit-langit mulut), bahkan daging dan tulangnya juga hitam. Sosok tubuh ayam kedu jantan dewasa tinggi besar dan bobotnya antara 3 Kg- 3,5 Kg, sedangkan yang betina dewasa berbobot antara 2 Kg- 2,5 Kg.<br />
                                        <br />
                                        c. Ayam Kedu Putih<br />
                                        <br />
                                        <div><a href="" imageanchor="1"><img src="images/Ayam Kedu Putih.jpg" alt="" width="150" height="214" border="0" /></a></div>
                                        <br />
                                        Ayam kedu putih ditandai dengan warna bulu putih mulus, jengger dan kulit mukanya berwarna merah, sedangkan kakinya berwarna putih atau kekuning-kuningan. Jenggernya tegak berbentuk wilah. Bobot ayam jantan kedu putih dewasa mencapai 2,5 Kg. Sedangkan bobot ayam kedu putih betina 1,2 Kg – 1,5 Kg.<br />
                                        <br />
                                        d. Ayam Kedu Merah<br />
                                        <br />
                                        <div><a href="http://4.bp.blogspot.com/-mPPpwkryJeM/TtHk_B9Q90I/AAAAAAAABpY/tbiO5AgIJUk/s1600/Ayam%2BKedu%2BMerah.png" imageanchor="1"><img src="images/Ayam Kedu Merah.png" alt="" width="212" height="320" border="0" /></a></div>
                                        <br />
                                        <br />
                                        Ayam kedu merah ditandai dengan warna bulu hitam mulus, tetapi kulit muka dan jengger berwarna merah, sedangkan kulit badannya berwarna putih. Sosok tubuh ayam kedu merah tinggi besar dengan bobot ayam jantan dewasa 3 Kg-3,5 Kg, Sedangkan bobot ayam betina 2 Kg-2,5Kg.<br />
                                        <br />
                                        2. Ayam Nunukan<br />
                                        <br />
                                        <div><a href="http://3.bp.blogspot.com/-4QO8Db1SJAk/TtHlUGxrUMI/AAAAAAAABpk/W-VXTFixd2s/s1600/Ayam%2BNunukan.jpg" imageanchor="1"><img src="images/Ayam Nunukan.jpg" alt="" width="170" height="195" border="0" /></a></div>
                                        <br />
                                        Ayam nunukan disebut juga ayam Tawao. Ayam ini merupakan ayam lokal yang berkembang dipulau Tarakan, Kalimantan Timur. Ayam nunukan diperkirakan berasal dari Cina. Karakteristik ayam nunukan adalah warna bulunya merah cerah atau merah kekuning-kuningan, bulu sayap dan ekor tidak berkembang sempurna. Sementara paruh dan kakinya berwarna kuning atau putih kekuning-kuningan dengan jengger dan pial berwarna merah cerah. Jenggernya berbentuk wilah dan bergerigi delapan.<br />
                                        <br />
                                        Stadium anak ayam sampai umur 45 hari cenderung berbulu kapas. Berat badan ayam nunukan jantan dewasa 3,4 Kg – 4,2 Kg, sedangkan yang betina 1,6 Kg – 1,9 Kg.<br />
                                        <br />
                                        3. Ayam Pelung<br />
                                        <br />
                                        <div><a href="http://1.bp.blogspot.com/-UGbgl7sHDpc/TtHloDs5MfI/AAAAAAAABpw/TgO0GSHvUUU/s1600/Ayam%2BPelung.jpg" imageanchor="1"><img src="images/Ayam Pelung.jpg" alt="" width="320" height="206" border="0" /></a></div>
                                        <br />
                                        Ayam pelung merupakan ayam lokal yang berkembang di Kabupaten Cianjur dan Sukabumi (Jawa Barat). Ayam pelung memiliki sosok tubuh besar dan tegap, temboloknya tampak menonjol. Kakinya panjang, kuat, dan pahanya berdaging tebal. Ayam pelung jantan memiliki Jengger berbentuk wilah yang besar, tegak, bergerigi nyata dan berwarna merah cerah. Ayam pelung betina mempunyai jengger, tetapi jengger terseebut tidak berkembang dengan baik. Ayam pelung jantan dewasa mempunyai bobot badan berkisar antara 3,5 Kg – 5,5 Kg, sedangkan yang betina 2,5 Kg – 3,5 Kg.<br />
                                        <br />
                                        4. Ayam Sumatra<br />
                                        <br />
                                        Ayam Sumatra merupakan ayam lokal dari Sumatra Barat. Penampilan perawakannya tegap, gagah ,tetapi ukuran tubuhnya kecil. Ayam Sumatra jantan berkepala kecil, tetapi tengkoraknya lebar. Pipinya penuh (padat), keningnya tebal, dan pialnya menggantung ke bawah. Paruh ayam Sumatra umumnya pendek dan kukuh berwarna hitam, dengan cuping kecil dan berwarna hitam. Ayam Sumatra memiliki jengger berbentuk wilah dan berwarna merah. Kulit muka juga berwarna merah atau hitam, ditumbuhi bulu halus yang jarang. Bobot ayam Sumatra jantan dewasa 2 Kg, sedangkan yang betina 1,5 Kg.<br />
                                        <br />
                                        5. Ayam Belenggek<br />
                                        <br />
                                        <div><a href="http://1.bp.blogspot.com/-82Um1R2VHME/TtHmSRwYPZI/AAAAAAAABp8/LL8yMsKDtsY/s1600/Ayam%2BBelenggek.jpg" imageanchor="1"><img src="images/Ayam Belenggek.jpg" alt="" width="320" height="240" border="0" /></a></div>
                                        <br />
                                        Ayam belenggek berasal dari Sumatra Barat, tepatnya dipedalaman Kabupaten Solok. Ayam ini pandai berkokok dengan suara yang merdu dan iramanya bersusun-susun, panjang sampai terdiri atas 6-12 suku kata. Semakin panjang suku katanya, semakin panjang kokoknya.<br />
                                        <br />
                                        6. Ayam Gaok<br />
                                        <br />
                                        <div><a href="http://2.bp.blogspot.com/-wktjRvQi9bo/TtHmnIWSRMI/AAAAAAAABqI/hXJOdOs2nlw/s1600/Ayam%2BGaok.jpg" imageanchor="1"><img src="images/Ayam Gaok.jpg" alt="" width="275" height="320" border="0" /></a></div>
                                        <br />
                                        Ayam gaok bersal dari madura dan Pulau Puteran, Kabupaten Sumenep. Keistimewaan ayam gaok yaitu kokoknya memiliki suara panjang yang hampir sama dengan ayam pelung yang terdapat di Cianjur (Jawa Barat). Ayam Gaok jantan dewasa memiliki bobot badan mencapai 4 Kg, sedangkan yang betina 2 - 2,5 Kg. Ayam Gaok jantan memiliki tampilan tubuh besar, tegap dan gagah. Jenggernya besar berbentuk wilah dan berwarna merah, dengan pial yang besar dan warnanya merah. Kakinya berwarna kuning [6]. Bulunya didominasi oleh warna kuning kehijau-hijauan (wido), namun ada juga yang berwarna lain, seperti merah dan hitam<br />
                                        <br />
                                        <br />
                                        <strong>Ayam Ras Pedaging</strong><br />
                                        <br />
                                        <div><a href="http://1.bp.blogspot.com/-q-_iDM-AZNg/TtHqERbIilI/AAAAAAAABqU/LFIPWJlHU4Y/s1600/ayam%2Bbroiler.jpg" imageanchor="1"><img src="images/ayam broiler.jpg" alt="" width="320" height="231" border="0" /></a></div>
                                        <br />
                                        Ayam ras pedaging disebut juga broiler, yang merupakan jenis ras unggulan hasil persilangan dari bangsa-bangsa ayam yang memiliki daya produktivitas tinggi, terutama dalam memproduksi daging ayam. Sebenarnya ayam broiler ini baru populer di Indonesia sejak tahun 1980-an dimana pemerintah mencanangkan panggalakan konsumsi daging ruminansia yang pada saat itu semakin sulit keberadaannya. Hingga kini ayam broiler telah dikenal masyarakat Indonesia dengan berbagai kelebihannya. Hanya 5-6 minggu sudah bisa dipanen. Dengan waktu pemeliharaan yang relatif singkat dan menguntungkan, maka banyak peternak baru serta peternak musiman yang bermunculan diberbagai wilayah Indonesia.<br />
                                        <br />
                                        Ayam ras pedaging yang sehari-hari kita mengenalnya dengan sebutan ayam broiler, merupakan jenis ayam ras unggulan hasil dari persilangan ras ayam yang memiliki produktivitas dan ketahanan tinggi,terutama dalam produktivitas daging. Hanya dalam 5 - 6 minggu saja jenis ayam ini bisa dipanen, karena masa pemeliharaan yang relatif singkat ayam jenis ini sangat digemari oleh peternak tidak heran jika setiap tahunya muncul peternak-peternak musiman di wilayah Indonesia. Namun saat <strong>isu flu burung masuk pertama kali di Indonesia</strong> pada tahun 2005 produksi ayam jenis ini sempat menurun.<br />
                                        <br />
                                        <strong>Jenis Ayam Pedaging</strong><br />
                                        <br />
                                        Dengan berbagai macam strain ayam ras pedaging yang telah beredar dipasaran, peternak tidak perlu risau dalam menentukan pilihannya. Sebab semua jenis strain yang telah beredar memiliki daya produktifitas relatif sama. Artinya seandainya terdapat perbedaan, perbedaannya tidak menyolok atau sangat kecil sekali. <br />
                                        <br />
                                      Dalam menentukan pilihan strain apa yang akan dipelihara, peternak dapat meminta daftar produktifitas atau prestasi bibit yang dijual di Poultry Shoup. Adapun jenis strain ayam ras pedaging yang banyak beredar di pasaran adalah: Super 77, Tegel 70, ISA, Kim cross, Lohman 202, Hyline, Vdett, Missouri, Hubbard, Shaver Starbro, Pilch, Yabro, Goto, Arbor arcres, Tatum, Indian river, Hybro, Cornish, Brahma, Langshans, Hypeco-Broiler, Ross, Marshall&rdquo;m&rdquo;, Euribrid, A.A 70, H&amp;N, Sussex, Bromo, CP 707.</div>
